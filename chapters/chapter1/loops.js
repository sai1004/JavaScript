// ================== While loops ======================= //

var count = 0;

while (count <= 10) {
  console.log(count);
  count++;
}

// ================== do While loops ======================= //


var text = "";
var i = 0;
do {
  text += "The number is " + i + "\n";
  i++;
} while (i < 10);

console.log(text);

 